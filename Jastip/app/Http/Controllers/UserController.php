    public function update(Request $request)
    {
       
        //Kurang Validator
        $user = User::find($request->id_user);
        $saldo = $user->saldo;
        $saldo_baru = $saldo + $request->saldo_baru;
        User::where('id', $request->id_user)
            ->update([
                'saldo' => $saldo_baru
            ]);
        return redirect('produk')->with('status', 'Data Berhasil Diubah!');
    }

